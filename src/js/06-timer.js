class Timer {
  constructor({ onTick }) {
    this.intervalId = null;
    this.isActive = false;
    this.onTick = onTick; // –¥–µ—Å—Ç—Ä—É–∫—Ç—É—Ä–∏–∑–∏—Ä—É–µ–º —Å–≤–æ–π—Å—Ç–≤–æ onTick –ø–µ—Ä–µ–¥–∞–≤ –µ–≥–æ –≤ —Ñ—É–Ω–∫—Ü–∏—é constructor({onTick})

    this.init(); // –¥–æ–±–∞–≤–∏–º –≤ –æ–±—ä–µ–∫—Ç –Ω–∞—Å—Ç—Ä–æ–µ–∫ –Ω–æ–≤–æ–µ —Å–≤–æ–π—Å—Ç–≤–æ (–ø–æ–∫–∞–∑ –Ω—É–ª–µ–≤–æ–≥–æ —Å—á–µ—Ç—á–∏–∫–∞ –ø—Ä–∏ –æ—Ç–∫—Ä—ã—Ç–∏–∏ —Å—Ç—Ä–∞–Ω–∏—Ü—ã)
  }

  // –¥–æ–ø–æ–ª–Ω–∏–º –Ω–∞—à –∫–ª–∞—Å—Å —Ñ—É–Ω–∫—Ü–∏–æ–Ω–∞–ª–æ–º: –ø—Ä–∏ –æ—Ç–∫—Ä—ã—Ç–∏–∏ —Å—Ç—Ä–∞–Ω–∏—Ü—ã –≤ –Ω–∞—à–µ–º –∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å–µ –±—É–¥–µ—Ç —Å—Ä–∞–∑—É –¥–æ—Å—Ç—É–ø–µ–Ω —Å—á–µ—Ç—á–∏–∫ —Å –Ω–æ–ª—è–º–∏. 
  init() {
    const time = this.getTimeComponents(0);
    this.onTick(time);
  }

  start() {
    if (this.isActive) {
      return;
    }

    const startTime = Date.now();
    this.isActive = true;

    this.intervalId = setInterval(() => {
      const currentTime = Date.now();
      const deltaTime = currentTime - startTime;
      const time = this.getTimeComponents(deltaTime); // –Ω–∞—à –∫–ª–∞—Å—Å –¥–æ–ø–æ–ª–Ω–µ–Ω –º–µ—Ç–æ–¥–æ–º

      this.onTick(time); // –ø–æ—Å–ª–µ –¥–µ—Å—Ç—Ä—É–∫—Ç—É—Ä–∏–∑–∞—Ü–∏–∏ –ø–µ—Ä–µ–¥–∞–¥–∏–º —Å—Å—ã–ª–∫—É –Ω–∞ —Ñ—É–Ω–∫—Ü–∏—é updateClockface, –∫–æ—Ç–æ—Ä–∞—è –æ–±–Ω–æ–≤–∏—Ç –Ω–∞–º –∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å –ø–æ –∫–∞–∂–¥–æ–º—É —Ç–∏–∫—É
    }, 1000);
  }

  stop() {
    clearInterval(this.intervalId);
    this.isActive = false;
    // –¥–æ–ø–æ–ª–Ω–∏–º –Ω–∞—à—É —Ñ—É–Ω–∫—Ü–∏—é —Å—Ç–æ–ø: –ø—Ä–∏ –Ω–∞–∂–∞—Ç–∏–∏ –Ω–∞ –∫–Ω–æ–ø–∫—É –æ–±–Ω—É–ª—è–µ—Ç—Å—è —Å—á–µ—Ç—á–∏–∫  
    const time = this.getTimeComponents(0); 
    this.onTick(time);
  }

// —Ä–∞—Å—à–∏—Ä–∏–º –Ω–∞—à—É –º–æ–¥–µ–ª—å (–∫–ª–∞—Å—Å) –º–µ—Ç–æ–¥–∞–º–∏: 

// –ü—Ä–∏–Ω–∏–º–∞–µ—Ç –≤—Ä–µ–º—è –≤ –º–∏–ª–ª–∏—Å–µ–∫—É–Ω–¥–∞—Ö, –í—ã—Å—á–∏—Ç—ã–≤–∞–µ—Ç —Å–∫–æ–ª—å–∫–æ –≤ –Ω–∏—Ö –≤–º–µ—â–∞–µ—Ç—Å—è —á–∞—Å–æ–≤/–º–∏–Ω—É—Ç/—Å–µ–∫—É–Ω–¥, –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –æ–±—å–µ–∫—Ç —Å–æ —Å–≤–æ–π—Å—Ç–≤–∞–º–∏ hours, mins, secs, –ê–¥—Å–∫–∞—è 
// –∫–æ–ø–∏–ø–∞—Å—Ç–∞ —Å–æ —Å—Ç–µ–∫–∞ üí©
  getTimeComponents(time) {
    const hours = this.pad(
      Math.floor((time % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60)),
    );
    const mins = this.pad(Math.floor((time % (1000 * 60 * 60)) / (1000 * 60)));
    const secs = this.pad(Math.floor((time % (1000 * 60)) / 1000));

    return { hours, mins, secs };
  }

//  –ü—Ä–∏–Ω–∏–º–∞–µ—Ç —á–∏—Å–ª–æ, –ø—Ä–∏–≤–æ–¥–∏—Ç –∫ —Å—Ç—Ä–æ–∫–µ –∏ –¥–æ–±–∞–≤–ª—è–µ—Ç –≤ –Ω–∞—á–∞–ª–æ 0 –µ—Å–ª–∏ —á–∏—Å–ª–æ –º–µ–Ω—å—à–µ 2-—Ö –∑–Ω–∞–∫–æ–≤
  pad(value) {
    return String(value).padStart(2, '0');
  }
}

